        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, NOBTCDeployment nOBTCDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<NOBTCDeployment>().SendRequestAndWaitForReceiptAsync(nOBTCDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, NOBTCDeployment nOBTCDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<NOBTCDeployment>().SendRequestAsync(nOBTCDeployment);
        }

        public static async Task<NOBTCService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, NOBTCDeployment nOBTCDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, nOBTCDeployment, cancellationTokenSource);
            return new NOBTCService(web3, receipt.ContractAddress);
        }

